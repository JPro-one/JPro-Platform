package one.jpro.platform.flexbox;

/**
 * Defines how multiple flex lines are distributed along the cross axis.
 * Only has effect when there are multiple lines (i.e. wrap is enabled).
 */
public enum FlexAlignContent {
    FLEX_START,
    FLEX_END,
    CENTER,
    SPACE_BETWEEN,
    SPACE_AROUND,
    SPACE_EVENLY,
    STRETCH
}
